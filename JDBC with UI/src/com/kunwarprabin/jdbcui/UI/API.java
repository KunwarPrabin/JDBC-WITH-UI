    public void show_Students_In_JTable() throws ClassNotFoundException, SQLException {
        StudentDAOImpl impl = new StudentDAOImpl();
        List<Student> list = impl.getAll();
        DefaultTableModel mode1 = (DefaultTableModel) jTable_Display_Students.getModel();
        Object[] row = new Object[6];

        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getFirstName();
            row[2] = list.get(i).getLastName();
            row[3] = list.get(i).getEmail();
            row[4] = list.get(i).getAddedDate();
            row[5] = list.get(i).getModifiedDate();

            mode1.addRow(row);

        }

    }


    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
            Student s = new Student();
            s.setFirstName(txtFirstName.getText());
            s.setLastName(txtLastName.getText());
            s.setEmail(txtEmail.getText());
            if (!txtFirstName.getText().trim().equals("")) {
                studentDAO.insert(s);
                lblmesssage.setText("DATA ADDED SUCCESSFULLY");
                DefaultTableModel mode1 = (DefaultTableModel) jTable_Display_Students.getModel();
                mode1.setRowCount(0);
                show_Students_In_JTable();
            } else {
                lblmesssage.setText("Name Should not be left blank");
            }

        } catch (ClassNotFoundException | SQLException ce) {
            System.out.println(ce.getMessage());
        }
                
    }//GEN-LAST:event_btnInsertActionPerformed

