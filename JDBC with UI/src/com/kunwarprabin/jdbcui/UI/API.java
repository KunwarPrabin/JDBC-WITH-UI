    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            Student s = new Student();
            s.setId(Integer.parseInt(txtId.getText()));
            s.setFirstName(txtFirstName.getText());
            s.setLastName(txtLastName.getText());
            s.setEmail(txtEmail.getText());
            if (!txtFirstName.getText().trim().equals("")) {
                studentDAO.update(Integer.parseInt(txtId.getText()), s);
                lblmesssage.setText("DATA updated SUCCESSFULLY");
            } else {
                lblmesssage.setText("Name Should not be left blank");
            }
        } catch (ClassNotFoundException | SQLException ce) {
            System.out.println(ce.getMessage());
        }
        try {
            DefaultTableModel mode1 = (DefaultTableModel) jTable_Display_Students.getModel();
            mode1.setRowCount(0);
            show_Students_In_JTable();
        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {

            int option = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to delete", null, JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                studentDAO.delete(Integer.parseInt(txtId.getText()));
                DefaultTableModel mode1 = (DefaultTableModel) jTable_Display_Students.getModel();
                mode1.setRowCount(0);
                show_Students_In_JTable();
            } else {
                DefaultTableModel mode1 = (DefaultTableModel) jTable_Display_Students.getModel();
                mode1.setRowCount(0);
                show_Students_In_JTable();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

